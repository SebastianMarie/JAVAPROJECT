package com.company.lab2.PrintSigns.AppMain;

public class PrintSigns {
    public static void main(String[] args) {
        String patern1 = "   j   a   v     v   a   ";
        String patern2 = "   j  a a   v   v   a a  ";
        String patern3 = "j  j aaaaa   v v   aaaaa ";
        String patern4 = " jj a     a   v   a     a";
        System.out.println(patern1);
        System.out.println(patern2);
        System.out.println(patern3);
        System.out.println(patern4);

        String patern5 = "         ";
        String patern6 = " +-----+ ";
        String patern7 = "(| o o |)";
        String patern8 = " |  ^  | ";
        String patern9 = " | `-` | ";
        String patern10 = " +-----+ ";
        System.out.println(patern5);
        System.out.println(patern6);
        System.out.println(patern7);
        System.out.println(patern8);
        System.out.println(patern9);
        System.out.println(patern10);
    }
}
